package com.digital.art.stuidoz.etsybot.services.proxy.providers.json.pubproxy;

public class Support {
	
	private int https;
	private int get;
	private int post;
	private int cookies;
	private int referer;
	private int user_agent;
	private int google;
	public int getHttps() {
		return https;
	}
	public void setHttps(int https) {
		this.https = https;
	}
	public int getGet() {
		return get;
	}
	public void setGet(int get) {
		this.get = get;
	}
	public int getPost() {
		return post;
	}
	public void setPost(int post) {
		this.post = post;
	}
	public int getCookies() {
		return cookies;
	}
	public void setCookies(int cookies) {
		this.cookies = cookies;
	}
	public int getReferer() {
		return referer;
	}
	public void setReferer(int referer) {
		this.referer = referer;
	}
	public int getUser_agent() {
		return user_agent;
	}
	public void setUser_agent(int user_agent) {
		this.user_agent = user_agent;
	}
	public int getGoogle() {
		return google;
	}
	public void setGoogle(int google) {
		this.google = google;
	}
}
